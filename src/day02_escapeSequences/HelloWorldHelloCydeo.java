package day02_escapeSequences;

public class HelloWorldHelloCydeo { // This is a class header.

    public static void main(String[] args) { // This is a main method used for making our class (file) runnable.

        // new line: \n
        System.out.println("Hello World\nHello Cydeo"); // This is a print statement that prints your words on your console.


        // horizontal tab: \t
        System.out.println("\tJava Programming Language");

        // backslash: \\
        System.out.println("\\");

        // double quote: \*
        System.out.println("I like \"Wooden Spoon\"");

        /* These are all escape sequences:
            newline
            horizontal tab
            backslash
            double quote
                                        28/09/2022

                                        */


    }

}