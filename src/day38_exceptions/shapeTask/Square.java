package day38_exceptions.shapeTask;

public class Square extends Shape{

    private double side;

    public Square(double side) {
        setSide(side);
    }

    public double getSide() {
        return side;
    }

    public void setSide(double side) {
        if(side <= 0) {
            throw new InvalidShapeException("side of the square can not be negative or zero");
        }
        this.side = side;
    }

    @Override
    public double area() {
        return side * side;
    }

    @Override
    public double perimeter() {
        return 4 * side;
    }

}
